const month=3
switch (month) {
    case 1:
        console.log("Jan")
        break;
    case 2:
        console.log("Feb")
        break;
    case 3:
        console.log("Mar") //Mar
        break;
    default:
        console.log("Default") 
        break;
}
//If we are not using break in case and then all the rest of the code will be executed which are placed after a given case statement which
//evaluated to true except the default argument
//Switch is case-sensitive
